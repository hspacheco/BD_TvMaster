    private void btnInserirCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirCategoriaActionPerformed
        Categoria cat = new Categoria();
        CategoriasDAO cdao = new CategoriasDAO();

        cat.setNome(txtNomeCategoria.getText());

        if(txtNomeCategoria.getText().equals("")){
            JOptionPane.showMessageDialog(null, "A categoria não foi inserida");
        }else{
            cdao.create(cat);
            lerTabelaCategorias();
            
            comboCategoria.removeAllItems();
            lerComboCategorias();
            
            txtNomeCategoria.setText("");
        }
        
        
        
    }//GEN-LAST:event_btnInserirCategoriaActionPerformed

    private void btnInserirCanalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirCanalActionPerformed
        Canal can = new Canal();
        CanaisDAO cdao = new CanaisDAO();
        
        if((txtNomeCanal.getText()).equals("") || (txtPrecoCanal.getText()).equals("")){
            JOptionPane.showMessageDialog(null, "O canal não foi inserido, preencha todos os campos");
        }else{
            can.setNome(txtNomeCanal.getText());
            can.setClassificacao((comboClassificacao.getSelectedItem()).toString());
            can.setCategoria(Integer.parseInt(((Categoria)comboCategoria.getSelectedItem()).getID()));
            can.setPreco(Float.parseFloat(txtPrecoCanal.getText()));
            
            cdao.create(can);
            lerTabelaCanais();
        }
    }//GEN-LAST:event_btnInserirCanalActionPerformed

    private void btnInserirPlanoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirPlanoActionPerformed
        Plano p = new Plano();
        PlanosDAO pdao = new PlanosDAO();
        
        if((txtNomePlano.getText()).equals("")){
            JOptionPane.showMessageDialog(null, "O plano não foi inserido, preencha todos os campos");
        }else{
            p.setNome(txtNomePlano.getText());
            pdao.create(p);
            lerTabelaPlanos();
        }
    }//GEN-LAST:event_btnInserirPlanoActionPerformed

