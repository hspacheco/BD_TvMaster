    public void lerTabelaCanais(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaCanalResultados.getModel();
        CanaisDAO cdao = new CanaisDAO();
        
        modelo.setNumRows(0);
        
        for(Canal can: cdao.read()){
            modelo.addRow(new Object[]{
                can.getNumero(),
                can.getNome(),
                can.getClassificacao(),
                can.getCategoria(),
                can.getPreco()
            });
        }
    }
    
    public void lerTabelaPlanos(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaPlanoResultados.getModel();
        PlanosDAO pdao = new PlanosDAO();
        
        modelo.setNumRows(0);
        
        for(Plano p: pdao.read()){
            modelo.addRow(new Object[]{
                p.getID(),
                p.getNome()
            });
        }
    }
    
    public void lerTabelaCategorias(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaCategoriaResultados.getModel();
        CategoriasDAO cdao = new CategoriasDAO();
        
        modelo.setNumRows(0);
        
        for(Categoria cat: cdao.read()){
            modelo.addRow(new Object[]{
                cat.getID(),
                cat.getNome()
            });
        }
    }
    
    public void lerComboCategorias(){
        CategoriasDAO cdao = new CategoriasDAO();
  
        /*ComboBox irá armazenar um objeto no qual o método toString irá sobrescrevero retorno,
        assim retornando apenas o nome da Categoria para selecionar.
        */
        for(Categoria cat: cdao.read()){
                comboCategoria.addItem(cat);      
        }
    }

